    {
        if ($request['type'] == "Collections"){
            $data = Collection::whereRaw('LOWER(name) LIKE ? ', ['%'.trim(strtolower($request['query'])).'%'])
            ->get();
            return response()->json($data);
        }else{
            $data = User::whereRaw('LOWER(username) LIKE ? ', ['%'.trim(strtolower($request['query'])).'%'])
            ->get();
            return response()->json($data);
        }
    }

