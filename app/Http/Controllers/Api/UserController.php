    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $arr = array();
        $address = $request['address'];
        $balance = $request['balance'];
        $check = User::where('address', '=', $address)->first();
        if ($check === null){
            $data = User::create([
                'username'=>$address,
                'address'=>$address,
                'balance'=>$balance
            ]);
            if ($data){
                $arr['status'] = true;
                $arr['message'] = "success";
            }else{
                $arr['status'] = false;
                $arr['message'] = "failed";
            }
        }else{
            $arr['status'] = false;
            $arr['message'] = "user already exists";
        }
        
    }

